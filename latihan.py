# Input
n1 = int(input("Masukkan Nilai pertama: "))
n2 = int(input("Masukkan Nilai kedua  : "))
n3 = int(input("Masukkan Nilai ketiga : "))

# Proses
jumlah = n1 + n2 + n3
kurang = n1 - n2 - n3
kali = n1 * n2 * n3
bagi = n1 / n2 / n3

# Output
print("Jumlah dari n1 + n2 + n3 =", jumlah)
print("kurang dari n1 - n2 - n3 =", kurang)
print("kali   dari n1 * n2 * n3 =", kali)
print("bagi   dari n1 / n2 / n3 =", bagi)